string_var = "100"
integer_var = 42
float_var = 3.14 

string_to_int = int(string_var)

int_to_float = float(integer_var)

float_to_str = str(float_var)

print(f"string_var: {string_var}, type: {type(string_var)}")
print(f"string_to_int: {string_to_int}, type: {type(string_to_int)}")

print(f"integer_var: {integer_var}, type: {type(integer_var)}")
print(f"int_to_float: {int_to_float}, type: {type(int_to_float)}")

print(f"float_var: {float_var}, type: {type(float_var)}")
print(f"float_to_str: {float_to_str}, type: {type(float_to_str)}")